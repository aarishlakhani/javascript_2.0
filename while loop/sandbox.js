//while loop
//same as for loop but not all different statements are together
//only condition inside the parentheses

let i = 0;
while (i < 5) {
  console.log("in the loop", i);
  i++;
}

//array while loop
const names = ["aarish", "arshaan", "akbar"];
let j = 0;
while (j < names.length) {
  console.log("Hi! My name is ", names[j]);
  j++;
}
