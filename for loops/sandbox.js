//for loops
for (let i = 0; i < 5; i++) {
  console.log("in loop: ", i);
}
console.log("loop finished");

//array loops
const names = ["aarish", "arshaan", "akbar"];
for (let i = 0; i < names.length; i++) {
  console.log(i);
} //output 0 1 2

//accessing array elements
const names2 = ["aarish", "arshaan", "akbar"];
for (let i = 0; i < names.length; i++) {
  console.log(names2[i]);
} //output aarish arshaan akbar
