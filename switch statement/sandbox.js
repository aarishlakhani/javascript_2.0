//switch statement
const grade = "D";

switch (grade) {
  case "A":
    console.log("You got an A!");
    break;
  case "B":
    console.log("You got a B.");
    break;
  case "C":
    console.log("You got a C.");
    break;
  case "D":
    console.log("You got a D.");
    break;
  case "F":
    console.log("You got a F.");
    break;
  default:
    console.log("Not a valid grade");
}
